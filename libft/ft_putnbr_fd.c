#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	tmp[12];
	size_t	i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n == 0)
		write(fd, "0", 1);
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	i = 0;
	while (n)
	{
		tmp[i] = n % 10 + '0';
		n /= 10;
		i++;
	}
	while (i > 0)
		write(fd, &tmp[--i], 1);
}
